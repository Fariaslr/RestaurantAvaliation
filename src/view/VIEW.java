    private void avaliarGorjeta() {
        if (cboComida.getSelectedIndex() == 3 || cboService.getSelectedIndex() == 3) {
            lblGorjeta.setText("Gorjeta alta");
        } else if (cboService.getSelectedIndex() == 2) {
            lblGorjeta.setText("Gorjeta média");
        } else if (cboComida.getSelectedIndex() == 0 && cboService.getSelectedIndex() == 0) {
            lblGorjeta.setText("Gorjeta baixa");
        } else {
            lblGorjeta.setText("Gorjeta baixa");
        }
    }

    private void configurarComboBox() {
        cboComida.setBackground(null);
        cboService.setBackground(null);
    }
