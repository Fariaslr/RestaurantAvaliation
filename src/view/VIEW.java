    private void btnAvaliacaoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAvaliacaoActionPerformed
        if (cboComida.getSelectedIndex() == 0) {
            cboComida.setBackground(Color.red);
        } else if (cboService.getSelectedIndex() == 0) {
            cboService.setBackground(Color.red);
        } else if(JOptionPane.showConfirmDialog(this, "Deseja Avaliar?","Avaliação",JOptionPane.YES_NO_OPTION) == 0) {
            configurarComboBox();
            avaliarGorjeta();
        }
    }//GEN-LAST:event_btnAvaliacaoActionPerformed

